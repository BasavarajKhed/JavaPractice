package day4;

public class NumberIsPositiveOrNegative {

	public static void main(String[] args) {
	
		int num=0;
		//if Else Ladder
		if(num>0) {
			System.out.println("Positive Number");
		}
		else if(num<0) {
			System.out.println("Negative Number");
		}
		else {
			System.out.println("Number is zero");
		}
	}

}
